import Foundation

// MARK:- Variables
public extension Float {
    
    var intValue : Int{
        return Int(ceil(self))
    }
    
    var cleanValue: String {
        return self.truncatingRemainder(dividingBy: 1) == 0 ? String(format: "%.0f", self) : String(format: "%.2f", self)
    }
}
